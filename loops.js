//for loops

for (let i=0; i<3; i++){
    console.log(`This is iteration ${i}`);
}

const condiments = ['Ketchup', 'Mustard', 'Sauerkraut', 'Relish'];
for (let i = 0; i < condiments.length; i++){
    console.log(`Condiment: ${condiments[i]}`);
}

//while loops
let count = 5;
while (count < 10){
    console.log(`The count is: ${count}`);
    count++;
}